func TestGenerateSystemd_WithKafka(t *testing.T) {
	dir := t.TempDir()
	cfg := testConfig(true)

	paths, err := GenerateSystemd(cfg, "/opt/moca", SystemdOptions{
		OutputDir: dir,
		User:      "moca",
	})
	if err != nil {
		t.Fatalf("GenerateSystemd (kafka): %v", err)
	}

	// Expect 6 files: server@, worker@, scheduler, outbox, search-sync, target.
	if len(paths) != 6 {
		t.Fatalf("expected 6 files, got %d: %v", len(paths), paths)
	}

	// Verify key files exist and have correct content.
	serverData, _ := os.ReadFile(filepath.Join(dir, "moca-server@.service"))
	serverContent := string(serverData)
	for _, want := range []string{"[Unit]", "[Service]", "ExecStart=", "moca-server"} {
		if !strings.Contains(serverContent, want) {
			t.Errorf("server unit missing %q", want)
		}
	}

	targetData, _ := os.ReadFile(filepath.Join(dir, "moca.target"))
	targetContent := string(targetData)
	if !strings.Contains(targetContent, "moca-outbox.service") {
		t.Error("target missing outbox (kafka is enabled)")
	}
}

