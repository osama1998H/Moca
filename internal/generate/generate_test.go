func TestKafkaEnabled(t *testing.T) {
	t.Run("nil", func(t *testing.T) {
		if KafkaEnabled(config.KafkaConfig{Enabled: nil}) {
			t.Error("expected false for nil Enabled")
		}
	})
	t.Run("true", func(t *testing.T) {
		enabled := true
		if !KafkaEnabled(config.KafkaConfig{Enabled: &enabled}) {
			t.Error("expected true for *true Enabled")
		}
	})
	t.Run("false", func(t *testing.T) {
		enabled := false
		if KafkaEnabled(config.KafkaConfig{Enabled: &enabled}) {
			t.Error("expected false for *false Enabled")
		}
	})
}

func TestRenderToFile(t *testing.T) {
	dir := t.TempDir()
	// Nested directory that doesn't exist yet.
	path := filepath.Join(dir, "sub", "deep", "out.txt")

	err := renderToFile(path, "Hello {{.Name}}", map[string]string{"Name": "World"})
	if err != nil {
		t.Fatalf("renderToFile: %v", err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read: %v", err)
	}
	if string(data) != "Hello World" {
		t.Errorf("got %q, want %q", data, "Hello World")
	}
}

